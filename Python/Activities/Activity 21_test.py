import pytest

def test_addition():


    a = 20
    b = 30

    sum = a + b


    assert sum == 50


def test_subtraction():


    a = 80
    b = 10


    diff = a - b


    assert diff == 70


def test_multiplication():


    a = 29
    b = 34


    prod = a * b


    assert prod == 986


def test_division():


    a = 40
    b= 4

    quot = a / b


    assert quot == 10